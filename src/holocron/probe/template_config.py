"""Template configuration and registry utilities for probe-generated rules."""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Callable, Dict, List, Optional, Tuple


class EntityRelationship(Enum):
    """Supported entity relationships that influence rule generation."""

    INHERITANCE = "inheritance"
    ATTRIBUTE_ACCESS = "attribute_access"
    METHOD_CALL = "method_call"
    CONSTRUCTOR_CALL = "constructor_call"
    FUNCTION_CALL = "function_call"


class InferenceType(Enum):
    """Type of inference performed by a rule."""

    FORWARD_PASS = "forward"
    BACKWARD_PASS = "backward"
    BRIDGE_PASS = "bridge"


TemplateBuilder = Callable[..., List[Dict[str, Any]]]
RelationshipTemplateMap = Dict[EntityRelationship, Dict[InferenceType, TemplateBuilder]]


@dataclass
class TemplateConfig:
    """Base structure shared by all dynamically generated rules."""

    cwe: str
    finding_type: str
    rule_id_prefix: str
    message: str
    severity: str = "WARNING"
    languages: List[str] = field(default_factory=lambda: ["python"])
    mode: str = "taint"
    options: Dict[str, Any] = field(
        default_factory=lambda: {"taint_intrafile": True}
    )
    metadata_base: Dict[str, Any] = field(default_factory=dict)
    relationship_templates: RelationshipTemplateMap = field(default_factory=dict)

    def with_focus_on(self, focus: Optional[str]) -> "TemplateConfig":
        """Set taint_focus_on option when applicable."""

        if focus:
            merged = dict(self.options)
            merged["taint_focus_on"] = focus
            self.options = merged
        return self

    def add_metadata(self, **metadata: Any) -> "TemplateConfig":
        """Merge additional metadata fields."""

        merged = dict(self.metadata_base)
        merged.update(metadata)
        self.metadata_base = merged
        return self

    def register_template(
        self,
        relationship: EntityRelationship,
        inference_type: InferenceType,
        builder: TemplateBuilder,
    ) -> None:
        """Register template builder for given relationship/inference."""

        self.relationship_templates.setdefault(relationship, {})[
            inference_type
        ] = builder

    def get_template(
        self, relationship: EntityRelationship, inference_type: InferenceType
    ) -> Optional[TemplateBuilder]:
        """Fetch a template builder for the given relationship/inference."""

        return self.relationship_templates.get(relationship, {}).get(
            inference_type
        )

    def build_metadata(self) -> Dict[str, Any]:
        """Return metadata with CWE and finding type applied."""

        base = dict(self.metadata_base)
        base.setdefault("finding_type", self.finding_type)
        base.setdefault("cwe", [self.cwe])
        return base

    def create_rule_id(self, suffix: str) -> str:
        """Compute a namespaced rule id."""

        return f"{self.rule_id_prefix}-{suffix}"


def default_forward_config(
    cwe: str,
    rule_id_prefix: str,
    message: str,
    severity: str = "WARNING",
) -> TemplateConfig:
    """Helper for creating forward-pass template configs."""

    return (
        TemplateConfig(
            cwe=cwe,
            finding_type="source",
            rule_id_prefix=rule_id_prefix,
            message=message,
            severity=severity,
        )
        .with_focus_on("source")
        .add_metadata(description="Forward pass rule generated by probe.")
    )


def default_backward_config(
    cwe: str,
    rule_id_prefix: str,
    message: str,
    severity: str = "ERROR",
) -> TemplateConfig:
    """Helper for creating backward-pass template configs."""

    return TemplateConfig(
        cwe=cwe,
        finding_type="sink",
        rule_id_prefix=rule_id_prefix,
        message=message,
        severity=severity,
        options={"taint_intrafile": True, "symbolic_propagation": True},
    ).add_metadata(description="Backward pass rule generated by probe.")


__all__: Tuple[str, ...] = (
    "TemplateConfig",
    "EntityRelationship",
    "InferenceType",
    "TemplateBuilder",
    "RelationshipTemplateMap",
    "default_forward_config",
    "default_backward_config",
)

